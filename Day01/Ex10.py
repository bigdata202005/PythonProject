# 사칙연산
a = 10
b = 20

print(a + b)
print(a - b)
print(a * b)
print(a / b)        # 0.5
print(a // b)       # 0
print(a % b)        # 10
print(2 ** 10)      # 1024 : 누승을 구해준다.
print(10 / 3)       # 3.3333333333333335
print(10 // 3)      # 3
print(-10 / 3)      # -3.3333333333333335
print(-10 // 3)     # -4
# // 연산자 : 몫을 넘지 않는 최대 정수를 구해준다.
# ** 연산자 : 누승을 구해 준다.

